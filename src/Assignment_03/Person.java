package Assignment_03;

public class Person {

    // Variables fields
    String name;
    int age;

    // Constructor
    public Person(String PersonName, int PersonAge) {
        name = PersonName;
        age = PersonAge;
    }

    // method call
    public void printInfo() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
    }


}






